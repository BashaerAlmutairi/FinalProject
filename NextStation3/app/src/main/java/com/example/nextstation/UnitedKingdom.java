package com.example.nextstation;

public class UnitedKingdom {
    private String UKUniversity;
    private String UKButton;

    public UnitedKingdom(String UKUniversity, String UKButton) {
        this.UKUniversity = UKUniversity;
        this.UKButton = UKButton;
    }

    public String getUKUniversity() {
        return UKUniversity;
    }

    public void setUKUniversity(String UKUniversity) {
        this.UKUniversity = UKUniversity;
    }

    public String getUKButton() {
        return UKButton;
    }

    public void setUKButton(String UKButton) {
        this.UKButton = UKButton;
    }

}
